export function getTrendSignalForAesthetic(aesthetic: AestheticStyle): TrendSignalData | undefined {
  return TREND_SIGNALS.find((s) => s.aesthetic === aesthetic);
}

export function getTopTrendingAesthetics(count: number = 5): TrendSignalData[] {
  return [...TREND_SIGNALS]
    .sort((a, b) => b.currentVelocity - a.currentVelocity)
    .slice(0, count);
}

export function getRisingAesthetics(): TrendSignalData[] {
  return TREND_SIGNALS.filter(
    (s) => s.weeklyGrowth > 4 && s.currentVelocity < 80
  ).sort((a, b) => b.weeklyGrowth - a.weeklyGrowth);
}
